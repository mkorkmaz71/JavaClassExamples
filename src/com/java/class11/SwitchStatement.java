package com.java.class11;

public class SwitchStatement {
    public static void main(String[] args) {
        int rate = 3;

        switch (rate) {
            case 1:
                System.out.println("Awful service");
            case 2:
                System.out.println("Extremely poor service");
            case 3:
                System.out.println("Bad service");
        }

    }
}
